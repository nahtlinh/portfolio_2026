// 1. Navigation Active Scroll States & Mobile Toggle
function initScrollNav() {
  const nav = document.getElementById("main-nav");
  const navLinks = document.querySelectorAll(".nav-links a, .mobile-nav-links a");
  const sections = document.querySelectorAll("section");
  const mobileToggle = document.getElementById("mobile-nav-toggle");
  const mobileClose = document.getElementById("mobile-nav-close");
  const mobileOverlay = document.getElementById("mobile-nav-overlay");
  
  // Navbar sticky scroll effect
  window.addEventListener("scroll", () => {
    if (window.scrollY > 50) {
      nav.classList.add("scrolled");
    } else {
      nav.classList.remove("scrolled");
    }
  });

  // Active link highlighting on scroll
  const observerOptions = {
    root: null,
    rootMargin: "-20% 0px -60% 0px",
    threshold: 0
  };

  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        const id = entry.target.getAttribute("id");
        navLinks.forEach(link => {
          if (link.getAttribute("href") === `#${id}`) {
            link.classList.add("active");
          } else {
            link.classList.remove("active");
          }
        });
      }
    });
  }, observerOptions);

  sections.forEach(sec => observer.observe(sec));

  // Mobile navigation drawer toggle
  mobileToggle.addEventListener("click", () => {
    mobileOverlay.classList.add("open");
  });

  const closeMobileNav = () => {
    mobileOverlay.classList.remove("open");
  };

  mobileClose.addEventListener("click", closeMobileNav);
  
  document.querySelectorAll(".mobile-nav-links a").forEach(link => {
    link.addEventListener("click", closeMobileNav);
  });
}

// 2. Scroll Reveal Animations
function initScrollReveal() {
  const revealElements = document.querySelectorAll(".card-reveal");
  
  const revealObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add("revealed");
        revealObserver.unobserve(entry.target); // Reveal once
      }
    });
  }, {
    root: null,
    threshold: 0.1,
    rootMargin: "0px 0px -50px 0px"
  });

  revealElements.forEach(el => revealObserver.observe(el));
}

// 3. Floating Butterfly System
function initButterflies() {
  const container = document.getElementById("decor-container");
  const magicBtn = document.getElementById("spawn-butterfly-btn");
  
  // Initial spawning
  for (let i = 0; i < 4; i++) {
    setTimeout(() => spawnButterfly(), i * 3000);
  }

  // Auto spawn butterfly every 15s to keep it alive
  setInterval(() => {
    if (document.querySelectorAll(".floating-butterfly").length < 6) {
      spawnButterfly();
    }
  }, 12000);

  // Spawn butterfly on magic button click
  magicBtn.addEventListener("click", () => {
    spawnButterfly();
    // button click animation
    magicBtn.style.transform = "scale(0.85)";
    setTimeout(() => magicBtn.style.transform = "", 150);
  });

  function spawnButterfly() {
    const butterfly = document.createElement("div");
    butterfly.className = "floating-butterfly";
    
    // Position randomly along left edge or bottom
    const startFromBottom = Math.random() > 0.5;
    const startX = startFromBottom ? Math.random() * 80 : -5;
    const startY = startFromBottom ? 105 : Math.random() * 80;
    
    butterfly.style.left = `${startX}vw`;
    butterfly.style.top = `${startY}vh`;
    
    // Randomize path dynamics
    const duration = 12 + Math.random() * 8; // 12-20s
    butterfly.style.animation = `flutter-flight ${duration}s infinite linear`;
    
    // Build wings
    const wingLeft = document.createElement("div");
    wingLeft.className = "butterfly-wing left";
    const wingRight = document.createElement("div");
    wingRight.className = "butterfly-wing right";
    
    butterfly.appendChild(wingLeft);
    butterfly.appendChild(wingRight);
    
    // Hover micro-animation to flutter faster
    butterfly.addEventListener("mouseenter", () => {
      wingLeft.style.animationDuration = "0.05s";
      wingRight.style.animationDuration = "0.05s";
      butterfly.style.filter = "drop-shadow(0 0 15px rgba(240, 125, 245, 0.9))";
    });

    butterfly.addEventListener("mouseleave", () => {
      wingLeft.style.animationDuration = "0.2s";
      wingRight.style.animationDuration = "0.2s";
      butterfly.style.filter = "drop-shadow(0 4px 10px rgba(240, 125, 245, 0.5))";
    });

    // Tap butterfly to flutter away/disappear in sparkle
    butterfly.addEventListener("click", () => {
      butterfly.style.transform = "scale(0) translateY(-50px)";
      butterfly.style.opacity = "0";
      butterfly.style.transition = "all 0.5s ease";
      setTimeout(() => butterfly.remove(), 500);
    });

    container.appendChild(butterfly);

    // Clean up butterfly after flight finishes to prevent DOM bloat
    setTimeout(() => {
      if (butterfly.parentElement) {
        butterfly.remove();
      }
    }, duration * 1000);
  }
}

// 4. Project Grid Category Filters
function initFilters() {
  const filterBtns = document.querySelectorAll(".filter-btn");
  const projectCards = document.querySelectorAll(".project-card");

  filterBtns.forEach(btn => {
    btn.addEventListener("click", () => {
      // Toggle active button
      filterBtns.forEach(b => b.classList.remove("active"));
      btn.classList.add("active");

      const filterVal = btn.getAttribute("data-filter");

      projectCards.forEach(card => {
        const categories = card.getAttribute("data-categories").split(" ");
        
        // Clear any pending animation timeout to prevent rapid-click display bugs
        if (card.dataset.timeoutId) {
          clearTimeout(Number(card.dataset.timeoutId));
        }

        if (filterVal === "all" || categories.includes(filterVal)) {
          card.style.display = "flex";
          // Re-trigger animation
          const tid = setTimeout(() => {
            card.style.opacity = "1";
            card.style.transform = "scale(1)";
          }, 50);
          card.dataset.timeoutId = tid;
        } else {
          card.style.opacity = "0";
          card.style.transform = "scale(0.95)";
          const tid = setTimeout(() => {
            card.style.display = "none";
          }, 300);
          card.dataset.timeoutId = tid;
        }
      });
    });
  });
}

// 5. Interactive Modal & Gallery Carousel
function initModal() {
  const modal = document.getElementById("project-modal");
  const backdrop = document.getElementById("modal-backdrop");
  const closeBtn = document.getElementById("modal-close-btn");
  const projectCards = document.querySelectorAll(".project-card");
  
  // Slides navigation
  const prevBtn = document.getElementById("gallery-prev");
  const nextBtn = document.getElementById("gallery-next");

  const openModal = (projKey) => {
    const data = projectsData[projKey];
    if (!data) return;

    // Fill textual content
    document.getElementById("modal-title").textContent = data.title;
    document.getElementById("modal-date").innerHTML = `<i class="fa-regular fa-calendar"></i> ${data.date}`;
    document.getElementById("modal-overview").textContent = data.overview;
    document.getElementById("modal-badge").textContent = data.badge;

    // Fill Roles
    const rolesContainer = document.getElementById("modal-roles");
    rolesContainer.innerHTML = "";
    data.roles.forEach(role => {
      const roleBlock = document.createElement("div");
      roleBlock.className = "role-block";
      roleBlock.innerHTML = `
        <h5><i class="fa-solid fa-circle-chevron-right"></i> ${role.title}</h5>
        <p>${role.desc}</p>
      `;
      rolesContainer.appendChild(roleBlock);
    });

    // Setup Gallery
    currentProjectSlides = data.slides;
    currentSlideIdx = 0;
    setupSlides();

    // Open Modal
    modal.classList.add("open");
    modal.setAttribute("aria-hidden", "false");
    document.body.style.overflow = "hidden"; // disable scroll
  };

  const closeModal = () => {
    modal.classList.remove("open");
    modal.setAttribute("aria-hidden", "true");
    document.body.style.overflow = ""; // enable scroll
  };

  projectCards.forEach(card => {
    card.addEventListener("click", () => {
      const projKey = card.getAttribute("data-project");
      openModal(projKey);
    });
  });

  closeBtn.addEventListener("click", closeModal);
  backdrop.addEventListener("click", closeModal);

  // Close modal with ESC key
  window.addEventListener("keydown", (e) => {
    if (e.key === "Escape" && modal.classList.contains("open")) {
      closeModal();
    }
  });

  // Slide controls
  prevBtn.addEventListener("click", () => {
    showSlide(currentSlideIdx - 1);
  });
  nextBtn.addEventListener("click", () => {
    showSlide(currentSlideIdx + 1);
  });
}

function setupSlides() {
  const slidesContainer = document.getElementById("gallery-slides");
  const dotsContainer = document.getElementById("gallery-dots");
  
  slidesContainer.innerHTML = "";
  dotsContainer.innerHTML = "";

  currentProjectSlides.forEach((slide, idx) => {
    // Image element
    const img = document.createElement("img");
    img.src = slide.src;
    img.alt = slide.cap;
    img.className = "gallery-slide-img" + (idx === 0 ? " active" : "");
    slidesContainer.appendChild(img);

    // Dot element
    const dot = document.createElement("div");
    dot.className = "gallery-dot" + (idx === 0 ? " active" : "");
    dot.addEventListener("click", () => showSlide(idx));
    dotsContainer.appendChild(dot);
  });

  updateCaption();
}

function showSlide(newIdx) {
  if (currentProjectSlides.length === 0) return;
  
  // Wrap index
  if (newIdx < 0) {
    newIdx = currentProjectSlides.length - 1;
  } else if (newIdx >= currentProjectSlides.length) {
    newIdx = 0;
  }

  // Toggle active image
  const imgs = document.querySelectorAll(".gallery-slide-img");
  imgs[currentSlideIdx].classList.remove("active");
  imgs[newIdx].classList.add("active");

  // Toggle active dot
  const dots = document.querySelectorAll(".gallery-dot");
  dots[currentSlideIdx].classList.remove("active");
  dots[newIdx].classList.add("active");

  currentSlideIdx = newIdx;
  updateCaption();
}

function updateCaption() {
  const captionEl = document.getElementById("gallery-caption");
  if (currentProjectSlides[currentSlideIdx]) {
    captionEl.textContent = `${currentSlideIdx + 1} / ${currentProjectSlides.length} • ${currentProjectSlides[currentSlideIdx].cap}`;
  } else {
    captionEl.textContent = "";
  }
}

// 6. Contact Form Simulation
function initContactForm() {
  const form = document.getElementById("contact-form");
  const statusMsg = document.getElementById("form-status-msg");

  form.addEventListener("submit", (e) => {
    e.preventDefault();
    
    // Simulate sending progress
    const submitBtn = form.querySelector(".btn-submit");
    const origHtml = submitBtn.innerHTML;
    submitBtn.disabled = true;
    submitBtn.innerHTML = `Sending... <i class="fa-solid fa-spinner fa-spin"></i>`;
    
    setTimeout(() => {
      // Simulate success
      statusMsg.style.display = ""; // Reset inline display styles if previously set
      statusMsg.className = "form-status success";
      statusMsg.textContent = "Thank you! Your message has been sent successfully.";
      form.reset();
      
      submitBtn.disabled = false;
      submitBtn.innerHTML = origHtml;

      // Clear message after 5 seconds by resetting CSS class instead of hardcoding display:none
      setTimeout(() => {
        statusMsg.className = "form-status";
      }, 5000);
    }, 1500);
  });
}
